# ========== 2. 工具函数：数值数组转字符串 ==========

def array_to_str(arr: np.ndarray) -> str:
    """
    把一维数组变成 '0.123456, 0.234567, ...' 的字符串，
    用在 prompt 里展示 1024 个数。
    """
    return ", ".join(f"{v:.6f}" for v in arr)


