# ========== 4. 主逻辑：读取 hdf5 并生成 jsonl ==========
def main():
    num_normal = len(TEMPLATES_NORMAL)
    num_verr = len(TEMPLATES_VAL_ERROR)
    total_templates = num_normal + num_verr

    print(f"普通模板: {num_normal} 个, 数值纠错模板: {num_verr} 个, 总计: {total_templates} 个")
    if total_templates == 0:
        raise ValueError("没有任何模板，请先在 TEMPLATES_* 里填入中文模板")

    # 打开 diff-sorp 的 h5 文件（group 结构）
    with h5py.File(H5_PATH, "r") as f:
        # 所有 group 的 key，例如 '0000'...'9999'
        all_keys = sorted(list(f.keys()))
        N = len(all_keys)
        print("总样本数:", N)

        # 取第一个样本看一下形状
        first_grp = f[all_keys[0]]
        sample_data0 = np.array(first_grp["data"][:, :, 0], dtype=np.float32)  # (T, X)
        T, X = sample_data0.shape
        print("单个样本 data 形状:", sample_data0.shape)

        # 时间索引：前两帧 + 下一帧
        T0_IDX = 0
        T1_IDX = 1
        T_NEXT_IDX = T1_IDX + 1
        assert T_NEXT_IDX < T, "时间步不足以取下一帧，请检查数据维度"

        # 每个模板用的样本索引（简单地用前 NUM_SAMPLES_PER_TEMPLATE 条）
        if N < NUM_SAMPLES_PER_TEMPLATE:
            raise ValueError(f"样本数只有 {N} 条，不够每个模板用 {NUM_SAMPLES_PER_TEMPLATE} 条")
        base_indices = list(range(NUM_SAMPLES_PER_TEMPLATE))

        with OUTPUT_JSONL.open("w", encoding="utf-8") as fout:
            total_count = 0

            # ---------- 普通模板 ----------
            for tmpl in TEMPLATES_NORMAL:
                print("处理普通模板")
                for sample_idx in base_indices:
                    global_idx = sample_idx
                    key = all_keys[global_idx]
                    grp = f[key]

                    # 读取该样本的 data，并去掉最后一维 (T, X, 1) -> (T, X)
                    data = np.array(grp["data"][:, :, 0], dtype=np.float32)  # (T, X)

                    u_t0 = data[T0_IDX, :]   # (X,)
                    u_t1 = data[T1_IDX, :]   # (X,)
                    u_t2 = data[T_NEXT_IDX, :]  # (X,)

                    # 统一保留 6 位小数
                    u_t0 = np.round(u_t0, 6)
                    u_t1 = np.round(u_t1, 6)
                    u_t2 = np.round(u_t2, 6)

                    # label = [u(t0), u(t1)]
                    label_vec = np.concatenate([u_t0, u_t1]).tolist()
                    # completion = 下一帧 u(t2)
                    completion_vec = u_t2.tolist()

                    prompt = tmpl.format(
                        T0=T0_IDX,
                        T1=T1_IDX,
                        U_T0=array_to_str(u_t0),
                        U_T1=array_to_str(u_t1),
                    )

                    example = {
                        "prompt": prompt,
                        "label": label_vec,
                        "completion": completion_vec,
                    }
                    fout.write(json.dumps(example, ensure_ascii=False) + "\n")
                    total_count += 1

            # ---------- 数值纠错 模板 ----------
            for tmpl in TEMPLATES_VAL_ERROR:
                print("处理 数值纠错 模板")

                # 模板层面：固定一个错误位置（在当前使用的空间维度上）
                NX = X  # 空间点数，例如 1024
                idx_for_template = random.randint(0, NX - 1)

                for sample_idx in base_indices:
                    global_idx = sample_idx
                    key = all_keys[global_idx]
                    grp = f[key]

                    data = np.array(grp["data"][:, :, 0], dtype=np.float32)  # (T, X)

                    u_t0 = data[T0_IDX, :]
                    u_t1 = data[T1_IDX, :]
                    u_t2 = data[T_NEXT_IDX, :]

                    # round 到 6 位
                    u_t0 = np.round(u_t0, 6)
                    u_t1 = np.round(u_t1, 6)
                    u_t2 = np.round(u_t2, 6)

                    idx = idx_for_template
                    val_true = float(u_t0[idx])
                    val_wrong = float(np.round(val_true + 0.1, 6))  # 你可以改成乘几倍/几分之几

                    # 构造“带错误”的 t0 数组
                    u_t0_wrong = u_t0.copy()
                    u_t0_wrong[idx] = val_wrong
                    u_t1_wrong = u_t1  # t1 不动

                    # label 用正确的 t0 和 t1
                    label_vec = np.concatenate([u_t0, u_t1]).tolist()
                    completion_vec = u_t2.tolist()

                    prompt = tmpl.format(
                        T0=T0_IDX,
                        T1=T1_IDX,
                        IDX=idx + 1,  # 1-based
                        VAL_WRONG=f"{val_wrong:.6f}",
                        VAL_TRUE=f"{val_true:.6f}",
                        U_T0_WRONG=array_to_str(u_t0_wrong),
                        U_T1_WRONG=array_to_str(u_t1_wrong),
                    )

                    example = {
                        "prompt": prompt,
                        "label": label_vec,
                        "completion": completion_vec,
                    }
                    fout.write(json.dumps(example, ensure_ascii=False) + "\n")
                    total_count += 1

        print(f"生成完成，总共写入 {total_count} 条样本到 {OUTPUT_JSONL}")


if __name__ == "__main__":
    main()
